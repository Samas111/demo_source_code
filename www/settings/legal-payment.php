<h1>Podmínky plateb pro tvůrce</h1>
<div class="subtitle">Platné od 15.02.2026.</div>

<p>
Tyto podmínky upravují využívání platební funkcionality platformy Dancefy ze strany tvůrců.
Souhlasem potvrzujete, že rozumíte způsobu fungování platebního systému a přijímáte níže uvedené podmínky.
</p>

<div class="separator"></div>

<h2>1. Charakter služby</h2>
<p>
Dancefy poskytuje technologické řešení pro generování QR platebních kódů
na základě údajů zadaných tvůrcem.
</p>

<ul>
<li>Dancefy nevystupuje jako poskytovatel platebních služeb.</li>
<li>Platby probíhají přímo mezi účastníkem a tvůrcem.</li>
<li>Dancefy nepřijímá, nedrží ani nepřevádí finanční prostředky.</li>
</ul>

<h2>2. Role jednotlivých stran</h2>
<p>
Platební vztah vzniká přímo mezi tvůrcem a účastníkem openclass.
Dancefy poskytuje technické prostředí pro předání platebních údajů,
nikoliv samotné zpracování platby.
</p>

<p>
Tvůrce zejména zodpovídá za:
</p>

<ul>
<li>Správnost a aktuálnost zadaného bankovního účtu (IBAN).</li>
<li>Kontrolu přijatých plateb na svém účtu.</li>
<li>Potvrzení účasti po přijetí platby.</li>
<li>Řešení případných vratek nebo storna plateb.</li>
<li>Plnění svých daňových a zákonných povinností.</li>
</ul>

<h2>3. Omezení odpovědnosti</h2>
<p>
Dancefy nenese odpovědnost za samotný průběh bankovního převodu,
jelikož platba probíhá mimo platformu Dancefy,
prostřednictvím bankovních institucí.
</p>

<p>
Dancefy neodpovídá za:
</p>

<ul>
<li>Chybně zadané platební údaje.</li>
<li>Opožděné nebo neprovedené bankovní převody.</li>
<li>Spor mezi tvůrcem a účastníkem týkající se platby.</li>
</ul>

<p>
Dancefy však může v přiměřeném rozsahu poskytnout technickou součinnost
při řešení problémů souvisejících s generováním QR kódu.
</p>

<h2>4. Změna platebních údajů</h2>
<p>
Tvůrce odpovídá za správné nastavení bankovního účtu.
Změna IBAN může být technicky omezena z bezpečnostních důvodů.
</p>

<h2>5. Ochrana údajů</h2>
<p>
IBAN je uchováván výhradně pro technické účely generování platebního QR kódu.
Údaje nejsou poskytovány třetím stranám mimo rozsah nezbytný pro fungování systému.
</p>

<div class="separator"></div>

<h2>6. Porušení podmínek a sankce</h2>
<p>
V případě porušení těchto podmínek nebo jiného jednání,
které může poškodit uživatele nebo platformu Dancefy,
si Dancefy vyhrazuje právo bez předchozího upozornění:
</p>

<ul>
<li>Dočasně pozastavit účet tvůrce.</li>
<li>Trvale zrušit účet tvůrce.</li>
<li>Odebrat přístup do programu pro tvůrce.</li>
<li>Odstranit open classes nebo jiný obsah.</li>
<li>Zablokovat opětovnou registraci.</li>
</ul>

<p>
V případě podezření na podvodné jednání, zneužití systému nebo
jiné protiprávní aktivity si Dancefy vyhrazuje právo
spolupracovat s příslušnými orgány a uplatnit právní kroky
k ochraně svých práv a práv ostatních uživatelů.
</p>

